def slug(value):
    value = str(value or "").strip().lower()
    value = re.sub(r"[^a-z0-9._-]+", "-", value)
    return value.strip("-") or "product"


def title_case(value):
    return slug(value).replace("-", " ").replace("_", " ").title()


def clean_description(value, name):
    value = str(value or "").strip()
    return value or f"{title_case(name)} generated by Titan."


def product_root(name):
    root = (PRODUCTS / slug(name)).resolve()
    if not str(root).startswith(str(PRODUCTS.resolve())):
        raise ValueError("Invalid product path.")
    return root


