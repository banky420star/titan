def build_product(name, template="python_cli", description=""):
    name = slug(name)
    template = slug(template)
    description = clean_description(description, name)

    if template not in BUILDERS:
        return {"error": "Unknown product template.", "template": template, "available": template_names()}

    root = product_root(name)
    files = BUILDERS[template](name, description)
    write_files(root, files)

    meta = {"name": name, "template": template, "description": description, "files": sorted(files.keys())}
    (root / "product.json").write_text(json.dumps(meta, indent=2), encoding="utf-8")

    return {"result": "product built", "name": name, "template": template, "path": str(root), "files": sorted(files.keys())}
